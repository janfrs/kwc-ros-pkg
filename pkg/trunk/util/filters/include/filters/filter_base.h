#ifndef FILTERS_FILTER_BASE_H_
#define FILTERS_FILTER_BASE_H_

/** \brief A Base filter class to provide a standard interface for all filters
 * 
 */
template<typename T>
class FilterBase 
{
public:
  virtual ~FilterBase(){;};

  /** \brief Update filter mutating data in place
   * This will overwrite the results on top of the input
   */
  virtual bool update(T* data)=0;


  /** \brief Update the filter and return the data seperately
   */
  virtual bool update(const T* const data_in, T* data_out)=0;

};



#endif //#ifndef FILTERS_FILTER_BASE_H_
